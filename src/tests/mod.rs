use load_rom;

#[test]
fn load_rom_reads_bytes() {
    let rom = load_rom("src/tests/rom/", "load_registers.ch8");
    let bytes = vec![
        0x60, 0xFF,
        0x61, 0xEE,
        0x62, 0xDD,
        0x63, 0xCC,
        0x64, 0xBB,
        0x65, 0xAA,
        0x66, 0x99,
        0x67, 0x88,
        0x68, 0x77,
        0x69, 0x66,
        0x6A, 0x55,
        0x6B, 0x44,
        0x6C, 0x33,
        0x6D, 0x22,
        0x6E, 0x11,
        0x00, 0x00
    ];
    assert_eq!(bytes, rom);
}
